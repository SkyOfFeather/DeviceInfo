#import "DSDeviceInfo.h"
#import "sys/utsname.h"
#import <UIKit/UIKit.h>
@implementation DSDeviceInfo

//app 名称
+(NSString *)getAppName{
    NSDictionary *infoDictionary = [[NSBundle mainBundle] infoDictionary];
    NSString *app_Name = [infoDictionary objectForKey:@"CFBundleDisplayName"];
    return app_Name;
}

//app 版本号
+ (NSString *)getAppVersion{
    NSDictionary *infoDictionary = [[NSBundle mainBundle] infoDictionary];
    // app版本
    NSString *app_version = [infoDictionary objectForKey:@"CFBundleShortVersionString"];
    return app_version;
}

//app 版本号code  根据当前app_version 去掉点
+ (NSString *)getAppVersionCode{
    NSString *app_version = [self getAppVersion];
    return [app_version stringByReplacingOccurrencesOfString:@"." withString:@""];
}

//app Bulid
+ (NSString *)getAppBuild{
    NSDictionary *infoDictionary = [[NSBundle mainBundle] infoDictionary];
    NSString *app_build = [infoDictionary objectForKey:@"CFBundleVersion"];
    return app_build;
}
//获取bundleId
+ (NSString *)getBundleIdentifier{
    NSDictionary *infoDictionary = [[NSBundle mainBundle] infoDictionary];
    NSString *bundle_id = [infoDictionary objectForKey:@"CFBundleIdentifier"];
    return bundle_id;
}

// 手机别名：用户定义的名称
+ (NSString *)getUserPhoneName{
    return [[UIDevice currentDevice] name];
}
//手机系统版本
+ (NSString *)getSystemVersion{
   return [NSString stringWithFormat:@"iOS %@",[[UIDevice currentDevice] systemVersion]];
}

